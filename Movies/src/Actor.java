
public class Actor {
	   private String name;
	   private String surName;
	   
	public Actor(String name, String surName) {
		setName(name);
		setSurName(surName);
	}
	   
	   
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getSurName() {
		return surName;
	}
	public void setSurName(String surName) {
		this.surName = surName;
	}
	   


}
